package com.training.tekarch;

/*Program to print the result of the following operations
 * a= -5 + 8 * 6 
 * b= (55+9) % 9
 * c= 20 + -3 * 5/8
 * d= 5 + 15/3 * 2- 8%3
 */

public class Assignment1 {

	public static void main(String[] args) {


		int a= -5 + 8 * 6; 
		int b= (55+9) % 9;  
		float c= 20 + -3 * 5/8f;
		int d= 5 + 15/3 * 2- 8%3;
		
		System.out.println("Value of a is : "+ a + "\n");
		System.out.println("Value of b is : "+ b + "\n");
		System.out.println("Value of c is : "+ c + "\n");
		System.out.println("Value of d is : "+ d + "\n");

	}

}
