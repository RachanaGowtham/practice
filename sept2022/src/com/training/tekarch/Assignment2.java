package com.training.tekarch;

// Program to print the sum, addition, multiply, divide and remainder of 2 numbers

public class Assignment2 {
	
	public static void main(String[] args) {
		
		int n=100, m=4;
		
		int sum = n+m;
		int mul = n*m;
		int sub = n-m;
		int div = n/m;
		int remainder = n%m;
		
		
		System.out.println("Sum of two values is : "+ sum + "\n");
		System.out.println("Mul of two values is : "+ mul + "\n");
		System.out.println("Sub of two values is : "+ sub + "\n");
		System.out.println("Div of two values is : "+ div + "\n");
		System.out.println("Remainder of two values is : "+ remainder + "\n");
		
		
	}

}
