package com.training.tekarch;

// Program to print Area and Perimeter of a circle

public class Assignment3 {

	public static void main(String[] args) {
		
		float pie = 3.142f;
		int r = 6;
		
		float area = pie*r*r;
		float perimeter = 2*pie*r;
		
		System.out.println("Area and Perimeter of circle is : "+ area + "\t&\t" + perimeter);

	}

}
